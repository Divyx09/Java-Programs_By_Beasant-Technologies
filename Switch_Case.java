public class Switch_Case {
    public static void main(String[] args) {
        int num =10;

        switch(num){
            case 5:{System.out.println("num is 5");}
            case 10:{System.out.println("num is 10");}
            case 20:{System.out.println("num is 20");}
            case 50:{System.out.println("num is 50");}
            case 25:{System.out.println("num is 25");}
            case 15:{System.out.println("num is 15");}

            default:{System.out.println("No match found");}
        }

    }
}
